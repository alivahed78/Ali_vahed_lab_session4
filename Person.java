public class Person {
    String firstname , lastname;
    
    public Person(String firstname , String lastname){
        this.firstname = firstname;
        this.lastname = lastname;
    }
    
    public String getFirstname(){
        return firstname;
        
    }
    
    public String getLastname(){
        return lastname;
        
    }
    
    public String toString(){
        lastname.toString();
        firstname.toString();
        return null;
        
    }
}
